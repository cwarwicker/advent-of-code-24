  public void check_directions(String letter, int row, int col, String[][] data, int current_direction) {
    
    // If we go out of bounds, stop this search.
    if (row < 0 || col < 0 || row >= data.length || col >= data[row].length) {
      return;
    }
    
    // If the coords we are looking at don't contain the letter we want, stop this search.
    if (!data[row][col].equals(letter)) {
      return;
    }
    
    String next = "";
    
    if (letter.equals("X")) {
      next = "M";
    } else if (letter.equals("M")) {
      next = "A";
    } else if (letter.equals("A")) {
      next = "S";
    } else if (letter.equals("S")) {
      count = count + 1;
      return;
    }
    
    // We need to check in 8 directions, up left, up, up right, right, etc... all the way around.
    // However, we only want to check all directions if we're currently on an X. Otherwise, it's a specific direction we want to continue in.
    int[][] directions = new int[8][2];
    
    // Up and left.
    directions[0] = new int[]{row - 1, col - 1};
    
    // Up and up.
    directions[1] = new int[]{row - 1, col};
    
    // Up and right.
    directions[2] = new int[]{row - 1, col + 1};
    
    // Right.
    directions[3] = new int[]{row, col + 1};
    
    // Down and right.
    directions[4] = new int[]{row + 1, col + 1};
    
    // Down.
    directions[5] = new int[]{row + 1, col};
    
    // Down and left.
    directions[6] = new int[]{row + 1, col -1};
    
    // left.
    directions[7] = new int[]{row, col - 1};
    
    if (letter.equals("X")) {
      // Check each direction for the next letter.
      for (int d = 0; d < directions.length; d++) {
        check_directions(next, directions[d][0], directions[d][1], data, d);
      }
    } else {
      // If we've moved on from X, we want to continue in that direction.
      check_directions(next, directions[current_direction][0], directions[current_direction][1], data, current_direction);
    }

    
  }
  
  

